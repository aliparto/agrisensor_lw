// *****************************************************************************
#include "Delay.h"
// *****************************************************************************
void Delay_us( unsigned long us )
{
    __delay32((long)(GetInstructionClock()/1000000UL)*us);
}
// *****************************************************************************
void Delay_ms( unsigned long ms )
{	
    __delay32((long)(GetInstructionClock()/1000UL)*ms);
}
